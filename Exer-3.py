# Cálculo das expressões
# A
A1 = (4/2) + (2/4)
A2 = 4/2 + 2/4

# B
B1 = 4 / (2 + 2) / 4
B2 = 4 / 2 + 2 / 4

# C
C1 = (4 + 2) * 2 - 4
C2 = 4 + 2 * 2 - 4

# Impressão dos resultados
print("Resultados das expressões:")
print("A1 =", A1)
print("A2 =", A2)
print("B1 =", B1)
print("B2 =", B2)
print("C1 =", C1)
print("C2 =", C2)

# Verificação de igualdade
print("\nVerificações:")
print("A1 e A2 são iguais?", A1 == A2)
print("B1 e B2 são iguais?", B1 == B2)
print("C1 e C2 são iguais?", C1 == C2)
